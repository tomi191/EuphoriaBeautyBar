export interface BuildPromptInput {
  topic: string;
  keywords?: string[];
  /** Ако е зададена, моделът трябва да я използва; иначе сам избира. */
  category?: string;
}

export function buildMessages(
  input: BuildPromptInput,
): Array<{ role: "system" | "user"; content: string }> {
  const keywordLine =
    input.keywords && input.keywords.length > 0
      ? `Ключови думи, които да вплетеш естествено (без напъване): ${input.keywords.join(", ")}.`
      : "";

  const categoryLine = input.category
    ? `Категорията е зададена: "${input.category}". Използвай точно нея.`
    : `Сам избери най-подходящата категория от: ${BLOG_CATEGORIES.join(" | ")}.`;

  const userPrompt = [
    `Напиши блог статия на тема: "${input.topic}".`,
    keywordLine,
    categoryLine,
    "Пиши за салона Euphoria в кв. Левски, Варна. Заземи съветите в реална салонна практика — конкретни марки, техники, времена и стъпки, които наистина се случват при такава процедура. Не измисляй фалшиви случаи или клиентски истории.",
    "Върни само JSON обекта.",
  ]
    .filter(Boolean)
    .join("\n\n");

  return [
    { role: "system", content: SYSTEM_PROMPT },
    { role: "user", content: userPrompt },
  ];
}
